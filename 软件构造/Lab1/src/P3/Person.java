package P3;

/**
 * Person
 */
public class Person {
	private String name;

	/**
	 * Person Constructor
	 *
	 * @param name the person's name
	*/
	public Person(String name) {
		this.name = name;
	}

	/**
	 * Get the name of the person.
	 *
	 * @return the name of the person
	*/
	public String getName() {
		return name;
	}
}
